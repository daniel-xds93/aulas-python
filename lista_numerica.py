valores = [33, 81, 54, 23, 44, 9, 3, 12]

maior = max(valores) # max -> retorna o maior valor da lista
menor = min(valores) # min -> retorna o menor valor da lista
soma = sum(valores) # sum -> soma todos os valores da lista
qtd_itens = len(valores) # len -> retorna a quantidade de itens na lista

# media dos valores da lista
media = soma / qtd_itens

print(f"maior valor {maior}")

print(f"menor valor {menor}")

print(f"total somado {soma}")

print(f"quantidade de itens {qtd_itens}")

print(f"m√©dia de valores {media}")